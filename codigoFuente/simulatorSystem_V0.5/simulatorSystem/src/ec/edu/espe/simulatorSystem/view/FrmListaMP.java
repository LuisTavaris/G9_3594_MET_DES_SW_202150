   public void seleccionar(){
    int select = TablaMateriaP.getSelectedRow();
    FrmListaMateriaPrima mp  = new FrmListaMateriaPrima();
    mp.productoN = TablaMateriaP.getValueAt(select, 0).toString();
    mp.cantidadN= TablaMateriaP.getValueAt(select, 1).toString();
    mp.IDN= TablaMateriaP.getValueAt(select, 2).toString();
   mp.StockN= TablaMateriaP.getValueAt(select, 3).toString();
   this.setVisible(false);
 
  
    }
